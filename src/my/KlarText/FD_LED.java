    private void jCV_AnzeigeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCV_AnzeigeActionPerformed
        Boolean tabChange = false;
        int currCV = getCVfromIndexString(jCV_Anzeige, "CV#");
        jCV_Inhalt.setText("" + CV[1][currCV]);
        if( KTUI.checkNumRange( currCV, 5, 6 )) {
            if( currentTab != 2 )
                tabChange = true;
            currentTab = 2;
        }
        else if( currCV == 29 ) {
            if( currentTab != 0 )
                tabChange = true;
            currentTab = 0;
        }
        else if( KTUI.checkNumRange( currCV, 33, 62 )) {
            if( currentTab != 1 )
                tabChange = true;
            currentTab = 1;
        }
        if( tabChange ) {
            jDecodereigenschaften.setSelectedIndex(currentTab);
        }
}//GEN-LAST:event_jCV_AnzeigeActionPerformed

