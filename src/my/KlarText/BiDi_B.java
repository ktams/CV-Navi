    private void startIOAction() {
        bAbbruch = false;

        // set buttons for IO in progress
        changeButtons( false );

        // init progress bar
        jProgressBar1.setValue(0);
        jProgressBar1.setString(null);

        // set cursor to WAIT
        Cursor c = new Cursor(Cursor.WAIT_CURSOR);
        this.setCursor(c);

        //start timer
        timer.start();
    }

    private void stopIOAction() {
        // stop timer
        if( timer != null ) {
            timer.stop();
        }

        // set buttons to normal operation
        changeButtons( true );

        // set cursor
        Cursor c = new Cursor(Cursor.DEFAULT_CURSOR );
        setCursor(c);

        jProgressBar1.setString(null);
    }

    public void setStatus( int status ) {
        switch( status ) {
            case c.cuPowerOn:
                jCbStatus.setSelected( true );
                break;
            case c.cuPowerOff:
            default: // unknown status
                jCbStatus.setSelected( false );
                break;
        }
    }

